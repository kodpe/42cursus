#include <unistd.h>

void	ft_poudlard(char s)
{
	write(1, &s, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_poudlard(str[i]);
		i++;
	}
}
