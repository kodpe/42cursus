#include "tools.h"

char	*ft_get_nbr(char *str)
{
	int		i;

	i = 0;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
		i++;
	if (str[i] != '\0')
		return (0);
	if (i > 38 || i == 0)
		return (0);
	return (str);
}
